using Dr_Purple.Application.Services.Base;
using Dr_Purple.Domain.Interfaces;
using Dr_Purple.Domain.Repositories;
using MapsterMapper;
using Microsoft.Extensions.DependencyInjection;

namespace Dr_Purple.Application.Services.OrderServices.Handlers.Commands.Base;
public class BaseOrderCommandHandler : BaseHandler
{
    protected internal IOrderRepository? Repository;
    public BaseOrderCommandHandler(IServiceProvider? provider)
    : base(provider!.GetService<IUnitOfWork>(), provider!.GetService<IMapper>())
    {
        Repository = UnitOfWork!.OrderRepository;
    }
}

